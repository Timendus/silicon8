package silicon8

func (cpu *CPU) loadFont() {
  var font []uint8
	switch cpu.specType {
  case SCHIP, XOCHIP:
    font = schipFont[:]
  default:
    font = chip8Font[:]
  }
  copy(cpu.RAM, font)
}

